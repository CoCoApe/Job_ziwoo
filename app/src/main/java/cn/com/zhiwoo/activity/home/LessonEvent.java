package cn.com.zhiwoo.activity.home;



class LessonEvent {
    private String name;
    private Float price;
    private String url;

    public LessonEvent() {
    }

    LessonEvent(String name, Float price, String url) {
        this.name = name;
        this.price = price;
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Float getPrice() {
        return price;
    }

    public void setPrice(Float price) {
        this.price = price;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
